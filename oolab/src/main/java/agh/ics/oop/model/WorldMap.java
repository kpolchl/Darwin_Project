package agh.ics.oop.model;

public class WorldMap {
    private final int width;
    private final int height;
    public WorldMap(int width, int height) {
        this.width = width;
        this.height = height;
    }
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }




}
